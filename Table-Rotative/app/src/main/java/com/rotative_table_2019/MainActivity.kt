import android.widget.Toast
import com.rotative_table_2019.ui.main.Bluetooh
import com.rotative_table_2019.ui.main.SectionsPagerAdapter

class MainActivity : AppCompatActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main)
        val bluetooh: Bluetooh

        val sectionsPagerAdapter = SectionsPagerAdapter(this, supportFragmentManager)
        val viewPager: ViewPager = findViewById(R.id.view_pager)
        viewPager.adapter = sectionsPagerAdapter
        val tabs: TabLayout = this.findViewById(R.id.tabs)
        tabs.setupWithViewPager(viewPager)
        val fab: FloatingActionButton = findViewById(R.id.fab)

        fab.setOnClickListener { view ->
            startActivityForResult(bluetooh.enableBtIntent, 1)
            Snackbar.make(view, "Bluetooh activating", Snackbar.LENGTH_LONG)
                    .setAction("Action", null).show()
        }


    }


}